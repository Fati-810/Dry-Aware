/**
 * Get Single Slider Media with ajax
 */
function crocal_eutf_get_slider_media() {
	
	check_ajax_referer( 'crocal-eutf-get-slider-media', '_eutf_nonce' );

	if( isset( $_POST['attachment_ids'] ) ) {

		$attachment_ids = sanitize_text_field( $_POST['attachment_ids'] );

		if( !empty( $attachment_ids ) ) {

			$media_ids = explode(",", $attachment_ids);

			foreach ( $media_ids as $media_id ) {
				$slider_item = array (
					'id' => $media_id,
				);
				crocal_eutf_print_admin_media_slider_item( $slider_item, "new" );
			}
		}
	}
	if( isset( $_POST['attachment_ids'] ) ) { die(); }
}
add_action( 'wp_ajax_crocal_eutf_get_slider_media', 'crocal_eutf_get_slider_media' );


/**
 * Prints Single Slider Media  Item
 */
function crocal_eutf_print_admin_media_slider_item( $slider_item, $new = "" ) {
	$media_id = $slider_item['id'];

	$thumb_src = wp_get_attachment_image_src( $media_id, 'thumbnail' );
	$thumbnail_url = $thumb_src[0];
	$alt = get_post_meta( $media_id, '_wp_attachment_image_alt', true );

	$crocal_eutf_button_class = "eut-slider-item-delete-button";

	if( $new = "new" ) {
		$crocal_eutf_button_class = "eut-slider-item-delete-button eut-item-new";
	}

?>
	<div class="eut-slider-item-minimal">
		<input class="<?php echo esc_attr( $crocal_eutf_button_class ); ?> button" type="button" value="<?php esc_attr_e( 'Delete', 'crocal' ); ?>">
		<h3 class="hndle eut-title">
			<span><?php esc_html_e( 'Image', 'crocal' ); ?></span>
		</h3>
		<div class="inside">
			<input type="hidden" value="<?php echo esc_attr( $media_id ); ?>" name="_crocal_eutf_media_slider_item_id[]">
			<?php echo '<img class="eut-thumb" src="' . esc_url( $thumbnail_url ) . '" alt="' . esc_attr( $alt ) . '" width="120" height="120"/>'; ?>
		</div>
	</div>
<?php

}

/**
 * Prints Admin Option Selector
 */
function crocal_eutf_print_admin_option_wrapper_start( $item ) {

	$data_dependency = $item_highlight = $item_width = '';

	$item_type = crocal_eutf_array_value( $item, 'type' );
	$item_label = crocal_eutf_array_value( $item, 'label' );
	$item_required = crocal_eutf_array_value( $item, 'required' );
	$item_dependency = crocal_eutf_array_value( $item, 'dependency' );
	$item_multiple = crocal_eutf_array_value( $item, 'multiple' );
	$item_highlight = crocal_eutf_array_value( $item, 'highlight', 'standard' );
	$item_width = crocal_eutf_array_value( $item, 'width', 'normal' );
	$item_wrap_class = crocal_eutf_array_value( $item, 'wrap_class' );

	$wrapper_attributes = array();
	if( !empty( $item_dependency ) ) {
		$wrapper_attributes[] = "data-dependency='" . esc_attr( $item_dependency ) . "'";
	}

	$label_class = 'eut-label';
	if ( 'label' == $item_type ) {
		$label_class = 'eut-label eut-header-label';
	}

	$item_title = $item_desc = $item_info = '';

	if ( is_array ( $item_label ) ) {
		$item_title = crocal_eutf_array_value( $item_label, 'title' );
		$item_desc = crocal_eutf_array_value( $item_label, 'desc' );
		$item_info = crocal_eutf_array_value( $item_label, 'info' );
	} else {
		$item_title = $item_label;
	}

	//Classes
	$option_wrapper_classes = array( 'eut-fields-wrapper' );
	$option_wrapper_classes[] = 'eut-' . $item_highlight;
	if ( !empty ( $item_wrap_class ) ) {
		$option_wrapper_classes[] = $item_wrap_class;
	}
	$option_wrapper_class_string = implode( ' ', $option_wrapper_classes );

	$wrapper_attributes[] = 'class="' . esc_attr( $option_wrapper_class_string ) . '"';

?>
	<div <?php echo implode( ' ', $wrapper_attributes ); ?>>
		<div class="<?php echo esc_attr( $label_class ); ?>">
			<label>
				<span class="eut-title"><?php echo esc_html( $item_title ); ?></span>
				<span class="eut-description"><?php echo esc_html( $item_desc ); ?></span>
				<span class="eut-info"><?php echo esc_html( $item_info ); ?></span>
			</label>
		</div>
		<div class="eut-field-items-wrapper">
			<?php if ( '' == $item_multiple ) { ?>
			<div class="eut-field-item eut-field-item-<?php echo esc_attr( $item_width ); ?>">
			<?php } ?>

<?php
}

function crocal_eutf_print_admin_option_wrapper_end( $multiple = '' ) {
?>
			<?php if ( '' == $multiple ) { ?>
			</div>
			<?php } ?>
		</div>
	</div>

<?php
}

